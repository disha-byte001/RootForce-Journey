"""
Arithmetic operators are used to perform common mathematical operations
such as addition, subtraction, etc.
"""

a = 15
b = 5

print(a + b)        # Addition operator – adds two values
print(a - b)        # Subtraction operator – subtracts one operand from another (a and b are operands; symbol is the operator)
print(a * b)        # Multiplication operator – multiplies the operands
print(a / b)        # Division operator – divides the first operand by the second (returns float)
print(a // b)       # Floor division – divides and returns only the integer part
print(a % b)        # Modulus operator – returns the remainder
print(a ** b)       # Exponent operator – raises the first operand to the power of the second
