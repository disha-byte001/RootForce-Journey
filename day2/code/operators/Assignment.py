"""
Assignment operators are used to assign values to variables.
They combine arithmetic or bitwise operations with assignment.
"""

a = 10     # Here we assign 10 to the variable 'a' using '=' â†’ this is a basic assignment operator
b = 4        

a += b          # Same as a = a + b
print(a)

a -= b          # Same as a = a - b
print(a)

a *= b          # Same as a = a * b
print(a)

a /= b          # Same as a = a / b
print(a)

a //= b         # Same as a = a // b
print(a)

a %= b          # Same as a = a % b
print(a)

a **= b         # Same as a = a ** b (power operator)
print(a)
