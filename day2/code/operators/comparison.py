"""
Comparison operators are used to compare two values.
They return True or False based on the condition.
"""

a = 50
b = 75

print("a == b:", a == b)    # Checks if a is equal to b → False
print("a != b:", a != b)    # Checks if a is not equal to b → True
print("a > b:", a > b)      # Checks if a is greater than b → False
print("a < b:", a < b)      # Checks if a is less than b → True
print("a >= b:", a >= b)    # Checks if a is greater than or equal to b → False
print("a <= b:", a <= b)    # Checks if a is less than or equal to b → True